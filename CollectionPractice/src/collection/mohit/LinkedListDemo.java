package collection.mohit;

import java.util.LinkedList;

public class LinkedListDemo {
	public static void main(String[] args) {
		LinkedList l = new LinkedList();
		l.add(10);
		l.add(22);
		l.add("1");
		l.addFirst(222);
		System.out.println(l);
		l.addLast(220);
		System.out.println(l);
		
		
	}

}

